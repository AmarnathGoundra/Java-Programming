class Data_types
{
    public static void main(String a[])
    {
        byte b = 127;
        long e = 3;
        int d = 6;
        float marks = 75.6f;
        double m = 3.45;
        char c = 'K';
        boolean g = true;

        System.out.println(b);
        System.out.println(e);
        System.out.println(d);
        System.out.println(marks);
        System.out.println(m);
        System.out.println(c);
        System.out.println(g);
    }
}