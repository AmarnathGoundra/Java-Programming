public class loops1 
{
    public static void main(String args[])    
    {
        int i = 1;

        while(i<=5)
        {
            System.out.println("Hii");
            
            int j = 5;

            do
            {
                System.out.println("Hello");
                j++;
            }while(j<=3);

            i++;
            
        }
    }
}
