class assignment_operators 
{
    public static void main(String args[])   
    {
        int num1 = 7;
        num1 += 2;

        System.out.println(num1);

        num1--;
        System.out.println(num1);

        ++num1;
        System.out.println(num1);

        int num = 5;
        int result = num--;
        System.out.println(result);

        result = --num;
        System.out.println(result);

    }
}
